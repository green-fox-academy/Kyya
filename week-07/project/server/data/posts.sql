CREATE TABLE IF NOT EXISTS Posts(
  id SERIAL,
  title VARCHAR(128) NOT NULL,
  url TEXT NOT NULL,
  timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  score INT DEFAULT 0,
  PRIMARY KEY(id)
);

CREATE TRIGGER Before_Posts_Delete
  BEFORE DELETE ON Posts
  FOR EACH ROW
BEGIN
  DELETE FROM Votes WHERE pid = OLD.id;
END;

